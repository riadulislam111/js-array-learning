let uniqueNumbers = [];

for(let i = 0;  i < numbers.length; i++) {
    let exists = false;

    for(let j = 0; j < uniqueNumbers.length; j++){
        if(numbers[i] === uniqueNumbers[j]){
            exists = true;
            break;
        }
    }
    if(!exists) {
        uniqueNumbers.push(numbers[i]);
    }
}


